package com.findupon.commons.repository.datasource;

import com.findupon.commons.entity.datasource.PageMeta;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;


@Repository
public interface PageMetaRepo extends JpaRepository<PageMeta, Long> {

	@Query(value = "select * from page_meta where data_source_id = :dataSourceId limit 1", nativeQuery = true)
	PageMeta findByDataSourceId(@Param("dataSourceId") Long dataSourceId);
}
